.MODEL SMALL
.STACK 100H
.DATA
MSG  DB 0DH,0AH,'Enter a number: $'
MSG1 DB 0DH,0AH,'EVEN NUMBER$'
MSG2 DB 0DH,0AH,'ODD NUMBER$'

.CODE
MAIN PROC
MOV AX,@DATA
MOV DS,AX

LEA DX,MSG
MOV AH,09H
INT 21H

MOV AH,01H
INT 21H 
SUB AL , 30H

MOV Ah, 0
MOV BL, 2
DIV BL

CMP AH, 0
JZ EVEN 
JNZ ODD

EVEN:
LEA DX, MSG1  
JMP DISPLAY

ODD:
LEA DX, MSG2 
JMP DISPLAY

DISPLAY:
MOV AH,09H
INT 21H
MOV AH,4CH
INT 21H

MAIN ENDP
END MAIN
