.MODEL SMALL
.STACK 100H

.DATA
MSG DB 0DH, 0AH, 'Enter a number : $'

MSG1 DB 0DH, 0AH, 'SMALL$'
MSG2 DB 0DH, 0AH, 'LARGE$'
MSG3 DB 0DH, 0AH, 'INVALID$'

.CODE
MAIN PROC
MOV AX, @DATA
MOV DS, AX
    
LEA DX, MSG
MOV AH,9
INT 21H

MOV AH, 1
INT 21H         

CMP AL, '0'
JL INVALID

CMP AL, '9'
JG INVALID

CMP AL, '4'
JLE SMALL

JMP LARGE

SMALL:
LEA DX, MSG1    
JMP DISPLAY

LARGE:
LEA DX, MSG2
JMP DISPLAY

INVALID:
LEA DX, MSG3
JMP DISPLAY

DISPLAY:   
MOV AH, 09H
INT 21H
MOV AH, 4CH
INT 21H

MAIN ENDP
END MAIN
