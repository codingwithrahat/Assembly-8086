.MODEL SMALL
.STACK 100H
.DATA
MSG  DB 0DH,0AH,'Enter a char: $'
MSG1 DB 0DH,0AH,'VOWEL$'
MSG2 DB 0DH,0AH,'CONSONANT$'
MSG3 DB 0DH,0AH,'INVALID$'

.CODE
MAIN PROC
MOV AX,@DATA
MOV DS,AX

LEA DX,MSG
MOV AH,09H
INT 21H

MOV AH,01H
INT 21H

CMP AL,'a'
JL CHECK
CMP AL,'z'
JG CHECK
SUB AL,20H

CHECK:
CMP AL,'A'
JL INVALID
CMP AL,'Z'
JG INVALID

CMP AL,'A'
JE VOWEL
CMP AL,'E'
JE VOWEL
CMP AL,'I'
JE VOWEL
CMP AL,'O'
JE VOWEL
CMP AL,'U'
JE VOWEL

CONS:
LEA DX,MSG2
JMP DISPLAY

VOWEL:
LEA DX,MSG1
JMP DISPLAY

INVALID:
LEA DX,MSG3 
JMP DISPLAY

DISPLAY:
MOV AH,09H
INT 21H
MOV AH,4CH
INT 21H

MAIN ENDP
END MAIN
